# ==================== HELPER FUNCTIONS ====================

def get_text(key: str, lang: str = DEFAULT_LANG, **kwargs) -> str:
    """Get translated text for a given key and language."""
    translations = TRANSLATIONS.get(key, {})
    text = translations.get(lang, translations.get(DEFAULT_LANG, f"[{key}]"))
    if kwargs:
        try:
            text = text.format(**kwargs)
        except (KeyError, IndexError):
            pass
    return text


def get_lang_name(lang_code: str) -> str:
    """Get the display name for a language code."""
    lang_info = SUPPORTED_LANGUAGES.get(lang_code, {})
    return f"{lang_info.get('flag', '')} {lang_info.get('name', lang_code)}"


def get_all_lang_buttons() -> list:
    """Get list of (flag+name, lang_code) tuples for language selection buttons."""
    buttons = []
    for code, info in SUPPORTED_LANGUAGES.items():
        buttons.append((f"{info['flag']} {info['name']}", f"lang_{code}"))
    return buttons
